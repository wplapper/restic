func runOneDatabase(ctx context.Context, gopts GlobalOptions) error {

  var (
    repositoryData   RepositoryData
    dataBaseFilename string
    err              error
    startTime        time.Time
    recordCount      int
  )

  // startup
  startTime = time.Now()
  gOptions = gopts
  init_repositoryData(&repositoryData)
  recordCount = 0

  // convert Debug string to int
  debug_options_str := createDBOptions.Debug
  debug_options_int := 0
  re := regexp.MustCompile("^[a-fA-F0-9]+$")
  if re.FindString(debug_options_str) != "" {
    temp, _ := strconv.ParseInt(debug_options_str, 16, 64)
    debug_options_int = int(temp)
  }

  // step 1: open repository
  repo, err := OpenRepository(ctx, gopts)
  if err != nil {
    return err
  }
  Verboseff("Repository is %s\n", globalOptions.Repo)
  Verboseff("%-48s %7.1f seconds\n", "repository open",
    time.Since(startTime).Seconds())

  dataBaseFilename = selectDatabaseName(gopts)
  Verboseff("database is %s\n", dataBaseFilename)

  // check if file /home/wplapper/restic/all_index_repo.bin exists
  // if so, then create repositoryData.BlobToIndex[blob.ID] and
  // repositoryData.IndexToBlob first!
  err = processAllIndexRepoFile(&repositoryData)
  if err != nil {
    return err
  }

  // step 2: gather the base information
  err = gather_base_data_repo(repo, gopts, ctx, &repositoryData,
    createDBOptions.Timing)
  if err != nil {
    return err
  }
  Verboseff("%-48s %7.1f seconds\n", "gather base data",
    time.Since(startTime).Seconds())

  good, err := checkDatabaseDates(dataBaseFilename, repo, &repositoryData)
  if good {
    Printf("database is up-to-date\n")
    return nil
  }

  //Printf("db is '%s'\n", dataBaseFilename)
  _, err = os.Create(dataBaseFilename)
  if err != nil {
    Printf("Cant Create database - reason '%v'\n", err)
    return err
  }

  t1 := time.Now()
  sqlCmd := exec.Command("/usr/bin/sqlite3", dataBaseFilename)
  stdin, err := sqlCmd.StdinPipe()
  if err != nil {
    Printf("Can't Create Pipe to STDIN - reason '%v'\n", err)
    return err
  }

  go func() {
    defer stdin.Close()
    driver := &Driver{stdin: stdin, Debug: debug_options_int, RecordCount: &recordCount}
    // generate the input stream for the SQLite database
    GatherDatabaseData(ctx, repo, driver, &repositoryData, startTime)
  }()

  // wait for sqlite3 execution to finish
  out, err := sqlCmd.CombinedOutput()
  if err != nil {
    Printf("CombinedOutput failed - reason '%v'\n", err)
    Printf("%s\n", out)
    return err
  }
  if len(out) > 0 {
    Printf("%s\n", out)
  }

  Verboseff("%8d records for SQLite3 stream\n", recordCount)
  Verboseff("%-48s %7.1f seconds\n", "*** sqlite3 process ***",
    time.Since(t1).Seconds())
  return nil
}

func GatherDatabaseData(ctx context.Context, repo *repository.Repository,
  driver *Driver, repositoryData *RepositoryData, startTime time.Time) (err error) {
  /*
   * sequence of events is as follows:
   * the in-core file will be generated in the steps
   * 00-startup
   * 01-create_tables
   * 02-snapshots
   * 03-packfiles
   * 04-index_repo
   * 05-names
   * metaBlobSorter
   * 09-createSha56
   * 06-meta_data_storage (needs all the above dependencies)
   * 06-meta_data_storage (needs all the above dependencies)
   * 07-contents
   * 08-config (single row table)
   * 12-create_index
   * 13-finale
   *
   * The following tables can easily be created by running a few SQL statements:
   * children
   * snap_blobs
   * dir_names, for use in creating fullname
   */
  var sha256Content map[CmpIndex]int

  step_00_startup(driver)
  step_01_createTables(driver)
  step_02_snapshots(driver, repositoryData, startTime)              // 02-snapshots
  step_03_04_ix_files(driver, repositoryData, ctx, repo, startTime) // 03-packfiles, 04-index_repo
  namesMap := step_05_names(driver, repositoryData, startTime)
  metaBlobs := MetaBlobSorter(repositoryData)

  if createDBOptions.Sha256 {
    sha256Content = map[CmpIndex]int{}
  } else {
    sha256Content = step_09_createSha56(driver, repositoryData)
  }

  step_06_meta_data_storage(ctx, repo, driver, repositoryData, namesMap,
    metaBlobs, sha256Content, startTime)
  step_07_contents(driver, repositoryData, metaBlobs, startTime)
  step_08_repo_config(driver, repositoryData, repo, !createDBOptions.Sha256)
  step_12_createIndex(driver)

  step_13_finale(driver)
  Verboseff("%-48s %7.1f seconds\n", "end create stream",
    time.Since(startTime).Seconds())
  return nil
}

// start the export file
func step_00_startup(tx *Driver) {
  io.WriteString(tx.stdin, "PRAGMA foreign_keys=OFF;\n")
  io.WriteString(tx.stdin, "BEGIN EXCLUSIVE TRANSACTION;\n")
  *(tx.RecordCount) += 2
}

