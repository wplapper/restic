func processAllIndexRepoFile(repositoryData *RepositoryData) error {
  path := "/home/wplapper/restic/all_index_repo.bin"
  indexRepoStat, err := os.Stat(path)
  var (
    handle           *os.File
    pos              IntID
    err2             error
    youngestDatabase string
  )
  if err == nil {
    handle, err2 = os.Open(path)
    if err2 != nil {
      panic("unexpeced error after Stat(all_index_repo.bin)")
    }
  }
  defer handle.Close()

  maxTime := time.Date(2006, 1, 2, 15, 4, 5, 0, time.UTC)
  youngestDatabase = "???"
  fileList, err := filepath.Glob("/home/wplapper/restic/db/*.db")
  if err != nil {
    Printf("Can't glob /home/wplapper/restic/db/*.db - error is '%v'\n", err)
    return err
  }

  // loop over all databases
  for _, filename := range fileList {
    out, err := exec.Command("/usr/bin/sqlite3", "-noheader", filename,
      "SELECT last_index_time FROM config WHERE id=1").CombinedOutput()
    if err != nil { // ignore
      continue
    }

    // get rid of trailing "\n"
    out_str := string(out)
    out_str = out_str[:len(out_str)-1]
    youngIndexTime, err := time.Parse("2006-01-02 15:04:05", out_str)
    if err != nil {
      Printf("time.Parse %s - error2 '%v'\n", filepath.Base(filename), err)
      continue
    }

    // find youngest file
    if youngIndexTime.After(maxTime) {
      maxTime = youngIndexTime
      youngestDatabase = filename
    }
  }

  if maxTime.After(indexRepoStat.ModTime()) {
    Printf("WARN: %s is older than the youngest of the database file!\n", path)
    Printf("Youngest database file %s is from %s\n", youngestDatabase,
      maxTime.Format(time.DateTime))
    Printf("index_file %s\n", indexRepoStat.ModTime().Format(time.DateTime))
  }

  if handle != nil {
    pos = IntID(2)
    for true { // read from handle until we hit io.EOF
      var HexId restic.ID
      err = binary.Read(handle, binary.LittleEndian, &HexId)
      if err != nil && errors.Is(err, io.EOF) {
        handle.Close()
        break
      }
      _, ok := repositoryData.BlobToIndex[HexId]
      if !ok {
        repositoryData.BlobToIndex[HexId] = pos
        repositoryData.IndexToBlob = append(repositoryData.IndexToBlob, HexId)
        pos++
      }
    }
  }
  return nil
}

func checkDatabaseDates(dataBaseFilename string, repo *repository.Repository,
  repositoryData *RepositoryData) (bool, error) {
  changeTimes, err := findLatestChanges(repo, repositoryData)
  if err != nil {
    return false, err
  }

  out, err := exec.Command("/usr/bin/sqlite3", "-noheader", dataBaseFilename,
    "SELECT last_index_time FROM config WHERE id=1").Output()
  if err != nil { // ignore
    return false, err
  }

  // get rid of trailing "\n"
  out_str := string(out)
  out_str = out_str[:len(out_str)-1]
  youngIndexTimeDB, err := time.Parse("2006-01-02 15:04:05", out_str)
  if err != nil {
    Printf("time.Parse - error3 '%v'\n", err)
    return false, err
  }

  out, err = exec.Command("/usr/bin/sqlite3", "-noheader", dataBaseFilename,
    "SELECT count(*) FROM index_repo").Output()
  if err != nil { // ignore
    return false, err
  }

  // get rid of trailing "\n"
  out_str = string(out)
  out_str = out_str[:len(out_str)-1]
  countIndexRepo, _ := strconv.ParseInt(out_str, 10, 64)
  if int(countIndexRepo) != len(repositoryData.IndexHandle) {
    Printf("FAIL: count memory %7d\n", len(repositoryData.IndexHandle))
    Printf("FAIL: count dtbase %7d\n", countIndexRepo)
    return false, nil
  }

  // it is the nanoseconds which can play havoc here, so we have to revert
  // to compare string representations
  youngIndexRecordStr := changeTimes.youngIndexRecord.Format(time.DateTime)
  youngIndexDBStr := youngIndexTimeDB.Format(time.DateTime)
  if youngIndexRecordStr == youngIndexDBStr {
    return true, nil
  } else {
    Printf("FAIL: last index file   change %s\n", youngIndexRecordStr)
    Printf("FAIL: last database rec. index %s\n", youngIndexRecordStr)
    return false, nil
  }
}

// serialize 'content' step by step into sha256 by Write-ing() to it
func ConvertContent(content []IntID) (result restic.ID) {
  sha256sum := sha256.New()
  temp := make([]byte, 4)
  for _, data_blob_int := range content {
    //binary.LittleEndian.PutUint32(temp, data_blob_int): serialize uint32
    temp[0] = byte(data_blob_int)
    temp[1] = byte(data_blob_int >> 8)
    temp[2] = byte(data_blob_int >> 16)
    temp[3] = byte(data_blob_int >> 24)
    sha256sum.Write(temp)
  }

  // copy final target to [sha256.Size]byte,
  // type 'sha256sum.Sum(nil)' is []byte
  // 'copy' can copy between different types
  copy(result[:], sha256sum.Sum(nil))
  return result
}
